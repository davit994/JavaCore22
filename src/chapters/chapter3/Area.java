package chapters.chapter3;

public class Area {
    public static void main(String[] args) {
        double r = 10.8; // Radius okrujnosti
        double pi = 3.1416; // Priblizitelnoe znachenie cesla pi
        double a = pi * r * r; // Vichislit ploshad kruga
        System.out.println("Ploshad kruga = " + a);
    }
}
